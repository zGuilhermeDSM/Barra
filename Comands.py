''' Comandos  básicos usados no Git:

git init: Inicializa um novo repositório Git em um diretório

git clone [url]: Clona um repositório remoto para o seu ambiente local

git add [arquivo]: Adiciona um arquivo ou alterações de arquivos à área de staging (preparação para commit)

git commit -m "mensagem": Cria um commit com as alterações adicionadas e uma mensagem descritiva

git status: Mostra o estado atual do repositório, incluindo arquivos modificados, adicionados e não rastreados

git pull: Busca e incorpora mudanças de um repositório remoto para o seu repositório local

git push: Envia suas alterações locais para um repositório remoto

git branch: Lista as branches existentes no repositório. Você também pode usar git branch [nome] para criar uma nova branch

git checkout [branch]: Muda para a branch especificada. Você também pode usar git checkout -b [branch] para criar e mudar para uma nova branch

git merge [branch]: Mescla as alterações de outra branch na branch atual

git log: Mostra o histórico de commits do repositório

git diff: Exibe as diferenças entre os arquivos ou commits. Útil para ver o que mudou

git reset: Desfaz alterações. Pode ser usado para remover commits ou reverter arquivos para um estado anterior

git stash: Armazena temporariamente alterações não comitadas para que você possa trabalhar em outra coisa sem perder seu progresso

 '''